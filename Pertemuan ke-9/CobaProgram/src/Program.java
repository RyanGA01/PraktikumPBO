public class Program {

    public double luas(double r){ //lingkaran
        return 3.14 * r * r;
    }

    public double luas(double a, double t){ //segitiga
        return 0.5 * a * t;
    }

    public int luas(int s){ //persegi
        return s * s;
    }

    public int luas(int p, int l){ //persegi panjang
        return p * l;
    }
}
